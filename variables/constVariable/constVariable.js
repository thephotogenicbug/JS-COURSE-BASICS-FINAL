/*
    name variable is declared using let keyword
    if you create any variable using let keyword u can change there value 
    if you create any variable using const keyword u can't change there value 
*/
// let keyword example
let fname = "Naveen";
fname = "Kumar"
let msg = "Hello my name is " + fname + " i'm learning javascript"; // message variable

// Logging
console.log(msg); // statement

console.log("----------------------------------------------------------------")

/* const keyword example 
   if you declare any variable in const keyword it will be permament 
   u can't change / modify there value
*/ 
const name = "Naveen Kumar";
// name = "Kumar"
let message = "Hello my name is " + name + " i'm learning javascript"; // message variable

// Logging
console.log(message); // statement


