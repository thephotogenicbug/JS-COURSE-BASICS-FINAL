// function declaration
// make name variable dynamic by passing parameters
function greetUser(firstName, lastName) {
  // set of statement
  let msg = "Hello my name is " + firstName + "" + lastName + " i'm learning javascript";
  console.log(msg);
}

// call function
greetUser("Naveen", "Kumar"); // passing value ("Naveen") is called arguments 
greetUser("Jay", "Shree");
