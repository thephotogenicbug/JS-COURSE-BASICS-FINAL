// use square bracket [] to create an array by default it will be empty array
let favColors = ["red", "white", "black"]


// add value dynamically 
favColors[4] = "Orange"

// Print all the values in console
console.log(favColors)
console.log("------------------------------------")
// Print single value using index
console.log(favColors[0])
console.log("-------------------------------------")
// print length of the values
console.log(favColors.length)
